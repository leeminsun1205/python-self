# Math module
# sqrt: square root
print(sqrt(36))
# isqrt: integer square root
print(isqrt(16))
print(isqrt(45))
# pow: power
print(pow(2, 10))
print(pow(8, 1/3)) # tương đương căn bậc 3 của 2
# ceil: ceiling
print(ceil(2.3))
print(ceil(7.8))
# floor: flooring
print(floor(2.9))
# factorial: giai thua
print(factorial(100))
# gcd(a, b): greatest common divisor
print(gcd(100, 450))
# comb(n, k)
print(comb(10, 2))
# perm: chinh hop
print(perm(5, 3))
# fabs: float abs
print(fabs(-143.3545))

# built-in function
print(abs(-124))
print(round(5.3))
print(round(5.9535))
print(max(1, 3, 5, 2))
print(min(1, 3, 5, 2))
print(sum([1, 2, 3, 4, 5]))